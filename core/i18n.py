def language_from_settings(settings_manager) -> str:
    language = settings_manager.get("language", "en") if settings_manager else "en"
    return language if language in LANGUAGES else "en"


def make_translator(settings_manager):
    def translate(key: str, **kwargs) -> str:
        language = language_from_settings(settings_manager)
        text = TRANSLATIONS.get(language, {}).get(key)
        if text is None:
            text = TRANSLATIONS["en"].get(key, key)
        return text.format(**kwargs) if kwargs else text

    return translate
